# LIST (Shopping Cart)

shopping_cart = ["Milk", "Bread", "Eggs"]

print("Shopping Cart:", shopping_cart)


# TUPLE (Date of Birth)

dob = (22, "January", 2007)

print("Date of Birth:", dob)


# SET (Unique Student IDs)

student_ids = {101, 102, 103, 101}

print("Student IDs:", student_ids)


